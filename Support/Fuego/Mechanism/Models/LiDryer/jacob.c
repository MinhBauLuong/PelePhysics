  // third body variable declaration
  double thd;

  // pressure dependence variable declarations
  double k0;
  double kinf;
  double Pr;

  // troe variable declarations
  double logFcent;
  double A;
  double B;

  double logT = log(T);
  double m = rhoIwt;

  // reaction 4;
  pres_mod[0] = m + 1.5 * C[0] + 11.0 * C[2];

  // reaction 5;
